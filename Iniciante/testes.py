A = float(input())
B = float(input())
C = float(input())
pi = 3.14159
atri = (A * C) / 2
acir = pi * (C**2)
atra = ((A + B) * C) / 2 
aqua = B ** 2
aret = A * B
print(f'TRIANGULO: {atri:.3f}')
print(f'CIRCULO: {acir:.3f}')
print(f'TRAPEZIO: {atra:.3f}')
print(f'QUADRADO: {aqua:.3f}')
print(f'RETANGULO: {aret:.3f}')
